      <h5> Bagaimana Covid-19 menular</h5>
      <p>Cara penularan SARS-CoV-2 penyebab COVID-19 ialah melalui kontak dengan droplet saluran napas penderita.
        Droplet merupakan partikel kecil dari mulut penderita yang mengandung kuman penyakit, yang dihasilkan
        pada saat batuk, bersin, atau berbicara. Droplet dapat melewati sampai jarak tertentu (biasanya 1 meter).
        Droplet bisa menempel di pakaian atau benda di sekitar penderita pada saat batuk atau bersin.
        Namun, partikel droplet cukup besar sehingga tidak akan bertahan atau mengendap di udara dalam waktu yang lama.
        Oleh karena itu, orang yang sedang sakit, diwajibkan untuk menggunakan masker untuk mencegah penyebaran droplet.
        Untuk penularan melalui makanan, sampai saat ini belum ada bukti ilmiahnya.</p>
